CREATE TABLE IF NOT EXISTS sys_branch (
    id INT NOT NULL AUTO_INCREMENT,
    partybranchname VARCHAR(64) NOT NULL,
    establishtime DATE NULL,
    partynumber INT NULL,
    dept_id INT NULL,
    dept_name VARCHAR(64) NULL,
    branchuser_id INT NULL,
    branchuser_name VARCHAR(255) NULL,
    status CHAR(1) DEFAULT '0',
    create_by VARCHAR(64) DEFAULT '',
    create_time DATETIME NULL,
    update_by VARCHAR(64) DEFAULT '',
    update_time DATETIME NULL,
    remark VARCHAR(500) NULL,
    branch_secretary_id INT NULL,
    branch_secretary_name VARCHAR(255) NULL,
    PRIMARY KEY (id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

